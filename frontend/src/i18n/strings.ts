export function getString(
  key: string,
  locale: AppLocale,
  vars?: Record<string, string | number>,
): string {
  const raw = STRINGS[locale]?.[key] ?? STRINGS.en[key] ?? key;
  if (!vars) return raw;
  return raw.replace(/\{(\w+)\}/g, (_, name) => {
    const v = vars[name];
    return v === undefined || v === null ? `{${name}}` : String(v);
  });
}
