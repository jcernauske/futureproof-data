# ---------------------------------------------------------------------------
# Risk-level mapping. Deterministic per-boss thresholds (no dataset cuts).
# ---------------------------------------------------------------------------


def risk_level_for_boss(boss_id: BossId, raw_score: int | None) -> RiskLevel:
    """Map raw_score to advisory bucket per the §5 deterministic table.

    raw_score is None → "Insufficient" (NEVER "High"; the missing-data-
    is-not-zero rule per @fp-data-reviewer round-1 ruling).

    Otherwise:
        raw_score >= win_at_or_above   → "Low"
        raw_score >= draw_at_or_above  → "Moderate"
        raw_score >= floor(draw / 2)   → "Elevated"
        raw_score <  floor(draw / 2)   → "High"
    """
    if raw_score is None:
        return "Insufficient"
    spec = BOSS_SPECS[boss_id]
    if raw_score >= spec.win_at_or_above:
        return "Low"
    if raw_score >= spec.draw_at_or_above:
        return "Moderate"
    if raw_score >= floor(spec.draw_at_or_above / 2):
        return "Elevated"
    return "High"


def risk_level_from_outcome(boss_id: BossId, outcome: BossOutcome, raw_score: int | None) -> RiskLevel:
    """Convenience: map a BossFightResult shape to RiskLevel.

    Prefers the deterministic raw_score path; only falls back to outcome
    when raw_score is unavailable. "unknown" → Insufficient.
    """
    if outcome == "unknown":
        return "Insufficient"
    return risk_level_for_boss(boss_id, raw_score)


