def _risk_level_counts(build: Build) -> dict[RiskLevel, int]:
    """Tally RiskLevel across the 5 boss fights."""
    counts: dict[RiskLevel, int] = {
        "Low": 0, "Moderate": 0, "Elevated": 0, "High": 0, "Insufficient": 0,
    }
    for fight in build.gauntlet.fights:
        level = risk_level_from_outcome(fight.boss, fight.result, fight.raw_score)
        counts[level] += 1
    return counts


