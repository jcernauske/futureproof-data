def _ai_anchor(career: CareerOutcome) -> float | None:
    """AI exposure percentile (0..100). None when missing."""
    p = career.adoption_percentile
    if p is None:
        return None
    # Composite percentile is stored 0..1 (per ai_exposure_composite spec)
    # OR 0..100 depending on pipeline version. Normalize to 0..100.
    return p * 100 if p <= 1.0 else p


