"""Rubric scorer using Claude Opus 4.7 as the judge.

Five-axis rubric (relevance, specificity, voice, accuracy, length) scored 1-5
each. Mean of the five axes is the surface score. Why Claude, not Gemma:
Gemma judging Gemma is circular — judges can poke holes in our methodology.
Claude is external, capable, and the methodology is defensible.

The system prompt is static and large (~1.5K tokens with anchors and the
voice guide quote). Cached with ephemeral cache_control so repeat scoring
runs pay ~0.1x for the prefix.
"""

from __future__ import annotations

import json
import os
from typing import TYPE_CHECKING, Any

from pydantic import BaseModel, Field, ValidationError

from eval.scorers.base import GoldenCase, ScoreResult

if TYPE_CHECKING:
    import anthropic


_JUDGE_MODEL = "claude-opus-4-7"


class RubricAxis(BaseModel):
    """One axis of the five-axis rubric."""

    score: int = Field(..., ge=1, le=5)
    reason: str = Field(..., max_length=240, description="One sentence justifying the score.")


class RubricScore(BaseModel):
    """Full rubric output returned by the judge."""

    relevance: RubricAxis
    specificity: RubricAxis
    voice: RubricAxis
    accuracy: RubricAxis
    length: RubricAxis
    flagged_hallucinations: list[str] = Field(
        default_factory=list,
        description="Verbatim quotes from the response that contain fabricated facts.",
    )

    @property
    def mean(self) -> float:
        return (
            self.relevance.score
            + self.specificity.score
            + self.voice.score
            + self.accuracy.score
            + self.length.score
        ) / 5.0


_SYSTEM_PROMPT = """\
You are an external evaluator scoring AI-generated text from FutureProof, a
career-planning tool for high-school and early-college students. The text was
generated by a local Gemma 4 model. Your job is to score it on five axes
using the rubric below. Be strict — a score of 3 means "mediocre," not "fine."

# Voice guide (what good FutureProof copy sounds like)

Cool, confident, data-honest. We tell students what the data actually shows
without softening or moralizing. We don't sound corporate. We don't pad.
We treat 16-year-olds like adults who can handle real numbers — including
salary ranges, debt, layoffs, AI exposure, and other things adults sometimes
hide from kids. We use specific numbers and named entities (real careers,
real schools, real wages) instead of vague generalities.

Anti-patterns:
- "Could potentially open doors to a variety of opportunities" (vague)
- "Don't worry, you have plenty of time" (moralizing)
- "Many graduates go on to fulfilling careers" (no specifics)
- "It's important to consider all factors" (filler)

Good patterns:
- "Mean wage $87,400 in IL; top 25% earn $112k+"
- "12% of these jobs were lost to AI between 2022 and 2025"
- "Indiana University grads in this major have $34k median debt at graduation"

# Rubric

Score each axis 1-5.

## Relevance
Does the response address the actual context (the named career, school, stat,
or situation)?
- 1: Generic platitude that could apply to any career or major
- 2: Tangentially related but misses the specific context
- 3: Addresses the context but at a generic level
- 4: Specifically tied to the named entities
- 5: Specifically grounded in the named career AND the student's situation

## Specificity
Does the response use real data points vs. vague language?
- 1: No specifics ("good earning potential", "lots of options")
- 2: One vague specific, mostly generalities
- 3: Mix of specifics and generalities
- 4: Mostly specifics with concrete numbers, named entities
- 5: Heavy on real numbers ($87k, 12%, 5-year growth) and named careers/schools

## Voice
Does it match the FutureProof voice (per the voice guide above)?
- 1: Corporate, hedged, academic, or moralizing
- 2: Mostly off-voice with a few good moments
- 3: Neutral — doesn't sound corporate but doesn't sound like FutureProof either
- 4: Mostly on-voice with one or two slip-ups
- 5: Cool, confident, data-honest throughout

## Accuracy
Are all checkable facts verified against the input context? Flag fabrications.
- 1: Contains a clearly fabricated fact (school doesn't exist, wrong wage by >50%, made-up taxonomy code)
- 2: Mostly accurate but has at least one questionable claim
- 3: All claims are either accurate or unverifiable from context
- 4: All claims verifiable from context; accurate
- 5: All claims verifiable and accurate; no hallucinations of any kind

## Length
Is the response within its budget?
- 1: More than 2x over or under budget
- 2: 50-100% over or under budget
- 3: 25-50% over or under budget
- 4: Within 25% of budget
- 5: At or under budget with no padding

# Output format

Output ONLY valid JSON matching this schema. Each axis is an object with
`score` (1-5 integer) and `reason` (one sentence, max 240 chars).
`flagged_hallucinations` is a list of verbatim quotes from the response
containing fabricated facts; empty list if none.
"""


def _make_client() -> "anthropic.Anthropic":
    """Build an Anthropic client. ANTHROPIC_API_KEY must be set."""
    import anthropic  # lazy: most eval paths don't need the judge

    api_key = os.environ.get("ANTHROPIC_API_KEY")
    if not api_key:
        raise RuntimeError(
            "ANTHROPIC_API_KEY not set. Rubric scoring requires Claude as the judge."
        )
    return anthropic.Anthropic(api_key=api_key)


def _build_user_prompt(
    *,
    surface_name: str,
    surface_description: str,
    char_budget: int,
    input_context: str,
    actual_output: str,
) -> str:
    """Build the per-case user prompt. Static prefix is cached; this is the
    volatile part that varies per case."""
    return (
        f"# Surface\n{surface_name}\n\n"
        f"# Surface description\n{surface_description}\n\n"
        f"# Character budget\n{char_budget}\n\n"
        f"# Input context provided to the AI\n{input_context}\n\n"
        f"# AI-generated response\n{actual_output}\n\n"
        "Score this response on the five-axis rubric. Output JSON only."
    )


def score_rubric(
    *,
    case: GoldenCase,
    actual_output: str,
    surface: str,
    surface_description: str,
    char_budget: int,
    input_context: str,
    client: "anthropic.Anthropic | None" = None,
) -> ScoreResult:
    """Score one narrative case with Claude as the judge.

    Returns a ScoreResult with the mean of the five axes as the score (0-1
    normalized: mean / 5). Details include per-axis scores, reasons, and
    flagged hallucinations.

    On judge failure (parse error, API error), returns score=0 and passed=False
    with the error attached. Rubric scoring is best-effort — never crash the
    eval runner.
    """
    client = client or _make_client()

    user_prompt = _build_user_prompt(
        surface_name=surface,
        surface_description=surface_description,
        char_budget=char_budget,
        input_context=input_context,
        actual_output=actual_output,
    )

    try:
        response = client.messages.create(
            model=_JUDGE_MODEL,
            max_tokens=2048,
            system=[
                {
                    "type": "text",
                    "text": _SYSTEM_PROMPT,
                    "cache_control": {"type": "ephemeral"},
                }
            ],
            messages=[{"role": "user", "content": user_prompt}],
            output_config={
                "format": {
                    "type": "json_schema",
                    "schema": RubricScore.model_json_schema(),
                }
            },
        )
    except Exception as exc:  # noqa: BLE001 — judge failure must not crash the run
        return ScoreResult(
            case_id=case.case_id,
            surface=surface,
            metric="rubric",
            score=0.0,
            passed=False,
            error=f"judge_api_error: {type(exc).__name__}: {exc}",
        )

    text = next(
        (b.text for b in response.content if getattr(b, "type", None) == "text"),
        "",
    )
    if not text:
        return ScoreResult(
            case_id=case.case_id,
            surface=surface,
            metric="rubric",
            score=0.0,
            passed=False,
            error="judge_empty_response",
        )

    try:
        payload: dict[str, Any] = json.loads(text)
        rubric = RubricScore.model_validate(payload)
    except (json.JSONDecodeError, ValidationError) as exc:
        return ScoreResult(
            case_id=case.case_id,
            surface=surface,
            metric="rubric",
            score=0.0,
            passed=False,
            details={"raw_judge_output": text[:500]},
            error=f"judge_parse_error: {type(exc).__name__}: {exc}",
        )

    score_normalized = rubric.mean / 5.0
    usage = getattr(response, "usage", None)
    return ScoreResult(
        case_id=case.case_id,
        surface=surface,
        metric="rubric",
        score=score_normalized,
        passed=rubric.mean >= 3.5,
        details={
            "mean_score": rubric.mean,
            "axes": rubric.model_dump(),
            "judge_model": _JUDGE_MODEL,
            "cache_read_tokens": getattr(usage, "cache_read_input_tokens", None),
            "cache_write_tokens": getattr(usage, "cache_creation_input_tokens", None),
        },
        tokens_input=getattr(usage, "input_tokens", None) if usage else None,
        tokens_output=getattr(usage, "output_tokens", None) if usage else None,
    )
